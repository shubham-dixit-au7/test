/*
Question 2) -   Write a function that takes an integer minutes and converts it to seconds.
				Examples
				convert(5) ➞ 300
				convert(3) ➞ 180
				convert(2) ➞ 120
*/


//Answer- 
function convert(val){
  return val * 60;
}
console.log(convert(5)); //➞ 300
console.log(convert(3)); //➞ 180
console.log(convert(2)); //➞ 120