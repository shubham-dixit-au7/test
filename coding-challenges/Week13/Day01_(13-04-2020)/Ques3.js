/*
Question 3) -  	Create a function that takes two numbers as arguments and return their sum.
				Examples -
				addition(3, 2) ➞ 5
				addition(-3, -6) ➞ -9
				addition(7, 3) ➞ 10
*/


//Answer- 
function addition(val1, val2){
return val1 + val2
}
console.log(addition(3, 2)); //➞ 5
console.log(addition(-3, -6)); //➞ -9
console.log(addition(7, 3)); //➞ 10